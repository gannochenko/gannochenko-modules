export const propsBlocker = {
    shouldForwardProp: (
        propertyName: string | number | boolean,
        validate: (name: any) => boolean,
    ): boolean => {
        if (
            typeof propertyName !== 'string' ||
            (propertyName.startsWith('on') &&
                propertyName.length > 2 &&
                !(propertyName in allowedEventProps))
        ) {
            return false;
        }

        return validate(propertyName);
    },
};

export const getPropsBlocker = propsBlocker;
